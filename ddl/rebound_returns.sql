CREATE TABLE return_item_reason (
    "index" text,
    return_id text,
    return_item_sku_code text,
    "action" text,
    additional_title text,
    additional_value text,
    reason text,
    wish text,
    PRIMARY KEY ("index", return_id, return_item_sku_code),
    FOREIGN KEY (return_id) REFERENCES return_item(return_id),
    FOREIGN KEY (return_item_sku_code) REFERENCES return_item(sku_code)
);

