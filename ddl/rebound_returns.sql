    sku_code text,
    return_id text,
    cost_price text,
    cost_price_currency text,
    country_code text,
    dangerous_goods text,
    days_for_return text,
    dimensions_uom text,
    export_awb text,
    export_carrier_name text,
    export_date text,
    final_dest_addr_1 text,
    final_dest_addr_2 text,
    final_dest_addr_3 text,
    final_dest_city text,
    final_dest_company_name text,
    final_dest_country text,
    final_dest_id text,
    final_dest_name text,
    final_dest_neighborhood text,
    final_dest_state text,
    final_dest_suburb text,
    final_dest_zip text,
    final_disposition text,
    height text,
    hs_code text,
    img_path text,
    length text,
    non_returnable text,
    non_returnable_message text,
    price text,
    quantity text,
    sku_desc text,
    tracking text,
    verification_entity text,
    weight text,
    weight_uom text,
    width text,
    -- custom_* (dynamic column),
    PRIMARY KEY (sku_code, return_id),
    FOREIGN KEY (return_id) REFERENCES "return"(id)
